import numpy as np
from shapely.geometry import Polygon, LineString
from shapely.ops import unary_union
import matplotlib.pyplot as plt

def create_boustrophedon_coverage(polygon, line_spacing):
    minx, miny, maxx, maxy = polygon.bounds
    
    lines = []
    y = miny
    direction = 1
    
    while y <= maxy:
        if direction == 1:
            line = LineString([(minx, y), (maxx, y)])
        else:
            line = LineString([(maxx, y), (minx, y)])
        
        intersection = polygon.intersection(line)
        
        if not intersection.is_empty:
            if intersection.geom_type == 'MultiLineString':
                lines.extend(list(intersection.geoms))
            else:
                lines.append(intersection)
        
        y += line_spacing
        direction *= -1
    
    coverage_path = unary_union(lines)
    
    return coverage_path

def plot_coverage(polygon, coverage_path):
    fig, ax = plt.subplots(figsize=(12, 12))
    
    # Plot the polygon
    x, y = polygon.exterior.xy
    ax.plot(x, y, color='red', linewidth=2, label='Polygon')
    
    # Plot the coverage path
    for line in coverage_path.geoms:
        x, y = line.xy
        ax.plot(x, y, color='blue', linewidth=1, alpha=0.7)
    
    ax.set_aspect('equal')
    ax.legend()
    ax.set_title('Boustrophedon Coverage Map')
    plt.show()

